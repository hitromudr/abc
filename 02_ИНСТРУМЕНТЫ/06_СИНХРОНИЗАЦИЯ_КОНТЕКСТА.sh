#!/usr/bin/env bash

# ==============================================================================
# 06_СИНХРОНИЗАЦИЯ_КОНТЕКСТА.sh
# Точка входа для агента (Process Helix v2).
# Предыдущая реализация (Монолит) упиралась в лимиты Terminal (16KB) и `read_file` (Outline).
# Новая реализация: Скрипт валидирует наличие файлов и выдает Агенту
# жесткую инструкцию на ПАРАЛЛЕЛЬНОЕ чтение (Concurrent Tool Calls).
# ==============================================================================

set -uo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

# Удаление устаревшего кэша (если остался от v1.1)
CACHE_FILE="$PROJECT_ROOT/.context_cache.md"
[[ -f "$CACHE_FILE" ]] && rm -f "$CACHE_FILE"

# Полный граф системного контекста
CONTEXT_FILES=(
    ".rules"
    "00_ИНИЦИАЛИЗАЦИЯ/02_МАНИФЕСТ_ПРОЕКТА.md"
    "00_ИНИЦИАЛИЗАЦИЯ/03_ДВОЙНАЯ_СПИРАЛЬ_РАЗРАБОТКИ.md"
    "01_БАЗА_ЗНАНИЙ/01_АБСОЛЮТНЫЕ_ФИЛЬТРЫ_ВХОДА.md"
    "01_БАЗА_ЗНАНИЙ/02_КОГНИТИВНЫЙ_ЛАНДШАФТ.md"
    "01_БАЗА_ЗНАНИЙ/03_ИНЖЕНЕРНЫЙ_ОКТАГОН_ВЫЖИВАНИЯ.md"
    "01_БАЗА_ЗНАНИЙ/04_МАТРИЦА_КОНФЛИКТОВ_И_АРБИТРАЖ.md"
    "01_БАЗА_ЗНАНИЙ/05_ВЕКТОРЫ_МЫШЛЕНИЯ.md"
    "01_БАЗА_ЗНАНИЙ/06_ОРГАНИЧЕСКИЕ_СИСТЕМЫ.md"
    "02_ИНСТРУМЕНТЫ/01_АЛГОРИТМ_РАЗБОРА_ЗАДАЧИ.md"
    "02_ИНСТРУМЕНТЫ/02_ШАБЛОН_ИТОГОВОГО_ПРОТОКОЛА.md"
    "02_ИНСТРУМЕНТЫ/03_АВТОНОМНЫЙ_ПАЙПЛАЙН.md"
    "02_ИНСТРУМЕНТЫ/04_СЕССИОННЫЙ_АНАЛИЗАТОР.md"
    "02_ИНСТРУМЕНТЫ/05_УНИВЕРСАЛЬНЫЙ_ОПТИМИЗАТОР.md"
    "03_РЕШЕНИЯ/01_ОБЩИЕ_МЕТА_ПРОТОКОЛЫ/001_КОГНИТИВНАЯ_ДИСЦИПЛИНА.md"
)

echo "STATUS: ВАЛИДАЦИЯ КОНТЕКСТА (СИСТЕМА 2)"
echo "PROCESS HELIX: Переход на параллельное чтение (Concurrent Tool Calls) для обхода лимитов API."
echo "--------------------------------------------------------------------------------"

MISSING=0
VALID_PATHS=()

for file in "${CONTEXT_FILES[@]}"; do
    FILE_PATH="$PROJECT_ROOT/$file"
    if [[ -f "$FILE_PATH" ]]; then
        VALID_PATHS+=("abc/$file")
    else
        echo "[ERROR] Нарушена целостность базы: Файл не найден -> abc/$file" >&2
        MISSING=$((MISSING + 1))
    fi
done

if [[ $MISSING -gt 0 ]]; then
    echo "--------------------------------------------------------------------------------"
    echo "ФАТАЛЬНАЯ ОШИБКА ИНИЦИАЛИЗАЦИИ. ВОССТАНОВИТЕ ПРОПУЩЕННЫЕ ФАЙЛЫ."
    exit 1
fi

echo "ВСЕ УЗЛЫ (${#VALID_PATHS[@]} шт.) УСПЕШНО ПРОШЛИ ВАЛИДАЦИЮ НА ДИСКЕ."
echo ""
echo "=== ДИРЕКТИВА АГЕНТУ (ОБЯЗАТЕЛЬНО К ИСПОЛНЕНИЮ) ==="
echo "Для восстановления контекста, выполни ОДИН МАССИВНЫЙ ПАРАЛЛЕЛЬНЫЙ ВЫЗОВ"
echo "\`read_file\` для всех перечисленных ниже путей одновременно (без пауз):"
echo ""

for path in "${VALID_PATHS[@]}"; do
    echo "- $path"
done

echo ""
echo "Запрещено читать их по одному (это ведет к потере фокуса)."
echo "Вызови инструмент \`read_file\` для каждого файла в ОДНОМ ответе."
echo "==================================================="
